var a = 10;
var b = 5;

// Arthimetic
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

// Comparison
console.log(a > b);
console.log(a < b);
console.log(a == b);
console.log(a != b);
console.log(a >= b);
console.log(a <= b);

// Logical
var x = true;
var y = false;
console.log(x & y);
console.log(x || y);
console.log(!x & !y);

// Assignment
console.log(a += 5);
console.log(a -= 5);
console.log(a *= 5);




